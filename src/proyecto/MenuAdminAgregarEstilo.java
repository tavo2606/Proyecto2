        // TODO add your handling code here:
        Conexion();
        try{

            int idEstilo = Integer.parseInt(txtID.getText());
            String estilo = txtNombre.getText();

            s = connection.createStatement();
            int z = s.executeUpdate("INSERT INTO \"estilo\" (\"id_estilo\", \"nombre_estilo\") "
                + "VALUES ('"+idEstilo+"', '"+estilo+"') ");

            if (z == 1) {
                JOptionPane.showMessageDialog(null, "estilo agregado", "Agregado", JOptionPane.DEFAULT_OPTION);
            } else {
                JOptionPane.showMessageDialog(null, "estilo no agregado", "Error", JOptionPane.DEFAULT_OPTION);
            }

        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Archivo no encontrado " + e, "Error", JOptionPane.ERROR_MESSAGE);
        }
        limpiar();
