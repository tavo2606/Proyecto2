    private Integer idEstilo(){
        Conexion();
        int ide = 0;
        try{
        
            s = connection.createStatement();
            rs = s.executeQuery("SELECT MAX(\"id_estilo\") from estilo");
            try{
                while(rs.next()){
                    ide = Integer.parseInt(rs.getString("max")) + 1;
                }
            }catch(Exception e){
                ide = 1;
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Archivo no encontrado " + e, "Error", JOptionPane.ERROR_MESSAGE);
        }
        return ide;
    }
    
    private void limpiar(){
        txtID.setText(String.valueOf(idEstilo()));
        txtNombre.setText(null);
        txtNombre.requestFocus();
    }
    
    public void Conexion() {
        if (connection != null) {
            return;
        }

        String url = "jdbc:postgresql://localhost:5432/postgres";
        String password = "123";
        try {
            Class.forName("org.postgresql.Driver");
            connection = DriverManager.getConnection(url, "postgres", password);
            if (connection != null) {
                System.out.println("Connecting to database...");
            }
        } catch (Exception e) {
            System.out.println("Problem when connecting to the database");
        }
    }

